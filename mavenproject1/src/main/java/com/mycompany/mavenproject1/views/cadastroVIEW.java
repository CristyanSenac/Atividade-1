    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String produto = txtProduto.getText();
        String valorTxt = txtValor.getText();
        Double valor = null;

        if (produto.isEmpty() || valorTxt.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos");
            return;
        }

        try {
            valor = Double.valueOf(valorTxt);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "O valor deve ser num√©rico");
            return;
        }
        
        ProdutoDao dao = new ProdutoDao();
        StatusPedido status = dao.getStatusPedido(1);
        Produto produtoDto = new Produto(produto, valor, status);
        dao.salvar(produtoDto);
        JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso");
        this.atualizaListagem();
        
    }//GEN-LAST:event_btnSalvarActionPerformed

